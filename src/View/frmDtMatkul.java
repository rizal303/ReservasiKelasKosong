   private void crudDetail(String proses){
        txtDetail();
        InterfaceDtMatkul dtMatkul;
        dtMatkul=Factory.getDtMatkul();
        switch (proses) {
            case "INSERT":
                dtMatkul.insertDetail(e);
                break;
            case "UPDATE":
                e.setKdDetail(lblKdDetail.getText());
                dtMatkul.updateDetail(e);
                break;
            case "DELETE":
                e.setKdDetail(lblKdDetail.getText());
                dtMatkul.deleteDetail(e);
                break;
        }
           refreshText();
        tt();
    }
   
