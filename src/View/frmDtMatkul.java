    private void tabel(){
        tabelHeader=new String[]{"Detail","NIK","Dosen","Kode Matkul","Nama Matkul"};
        dtm=new DefaultTableModel (null, tabelHeader);
        tblDtMatkul.setModel(dtm);
        tampilTabel();
    }
    
    private void tampilTabel(){
        InterfaceDtMatkul dtMatkul;
        dtMatkul=Factory.getDtMatkul();
        listDaftar=dtMatkul.selectDtMatkul(e);
        dtm=(DefaultTableModel) tblDtMatkul.getModel();
        dtm.setRowCount(0);
        try{
            listDaftar.stream().forEach((d) -> {
                //ee=new EntEditData();
                dtm.addRow(new Object[]
                {d.getKdDetail(), d.getNik(),d.getNamaDosen(),d.getKdMatkul(),d.getNamaMatkul()});
            });
        int ambilBaris=tblDtMatkul.getRowCount();
        if(ambilBaris>0) tblDtMatkul.addRowSelectionInterval(0, 0);
        }
        catch(Exception ex){
            System.out.println(ex);
        }
    }
    
