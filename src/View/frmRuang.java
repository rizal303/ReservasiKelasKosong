    private void refresh(){
        lblKode.setText("");
        cmbGedung.setSelectedItem("-- pilih --");
        cmbLantai.setSelectedItem("-- pilih --");
        txtRuang.setText("");
        tampilTabel();
    }
    
    private void tabel(){
        tabelHeader=new String[]{"Kode","Gedung","Lantai","Nomer Urut"};
        dtm=new DefaultTableModel (null, tabelHeader);
        tblRuang.setModel(dtm);
        tampilTabel();
    }
    
    private void tampilTabel(){
        InterfaceRuang ruang;
        ruang=Factory.getRuang();
        listDaftar=ruang.selectRuang();
        dtm=(DefaultTableModel) tblRuang.getModel();
        dtm.setRowCount(0);
        try{
            for(EntitasRuang d:listDaftar){
            //ee=new EntEditData();
                dtm.addRow(new Object[]
            {d.getKode(),d.getGedung(),d.getLantai(),d.getNomor()});
        }
        int ambilBaris=tblRuang.getRowCount();
        if(ambilBaris>0) tblRuang.addRowSelectionInterval(0, 0);
        }
        catch(Exception ex){
            System.out.println(ex);
        }
    }
    
    private void valueChanged(){
        baris=tblRuang.getSelectedRow();
        Tanggal t=new Tanggal();
        if(baris>=0){
            lblKode.setText(dtm.getValueAt(baris, 0).toString());
            cmbGedung.setSelectedItem(dtm.getValueAt(baris, 1).toString());
            cmbLantai.setSelectedItem(dtm.getValueAt(baris, 2).toString());
            txtRuang.setText(dtm.getValueAt(baris, 3).toString());
        }
    }
    
    private void makeCenter(){
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        Dimension frameSize = this.getSize();
        if(frameSize.height > screenSize.height) {
            frameSize.height = screenSize.height;
        } if(frameSize.width > screenSize.width) {
            frameSize.width = screenSize.width;
        }
        this.setLocation((screenSize.width - frameSize.width) / 2,
        (screenSize.height - frameSize.height) / 2);
    }
    
