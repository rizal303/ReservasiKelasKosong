    public void tampilkan() {
        ImageIcon imageIcon = new ImageIcon("src/image/ruang.jpg"); // load the image to a imageIcon
        Image image = imageIcon.getImage(); // transform it 
        Image newimg = image.getScaledInstance(107, 79,  java.awt.Image.SCALE_DEFAULT); // scale it the smooth way  
        imageIcon = new ImageIcon(newimg);
        lblIkon.setIcon(imageIcon);
    }
    
    private void textRuang(){
        e.setGedung(cmbGedung.getSelectedItem().toString());
        e.setLantai(cmbLantai.getSelectedItem().toString());
        e.setNomor(txtRuang.getText());
    }
    
    private void crudRuang(String proses){
        textRuang();
        InterfaceRuang ruang;
        ruang=Factory.getRuang();
        switch (proses) {
            case "INSERT":
                e.setKode(e.getGedung()+"."+e.getLantai()+"."+e.getNomor());
                ruang.insertRuang(e);
                break;
            case "UPDATE":
                e.setKode(lblKode.getText());
                ruang.updateRuang(e);
                break;
            case "DELETE":
                e.setKode(lblKode.getText());
                ruang.deleteRuang(e);
                break;
            default:
                break;
        }
        refresh();
    }

